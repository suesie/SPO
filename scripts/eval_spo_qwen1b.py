#!/usr/bin/env python
"""Evaluate the SPO-tree DeepSeek-R1-Distill-Qwen-1.5B run on MATH-500 + OOD slate.

Slate:
  * MATH-500 (n=16) @ 2K context on ALL 40 ckpts (training-matched)
  * AIME-24 (n=32) @ 2K on final 4 ckpts (iter 925, 950, 975, 1000)
  * OlympiadBench (n=16) @ 2K on final 4 ckpts
  * CollegeMath (n=16) @ 2K on final 4 ckpts
  * MATH-500 (n=16) @ 4K context on final 4 ckpts (extrapolation row,
    matches SPO published "4K" column in README table)

Context-length notes:
  * SPO training had model_context_size=2048 + max_tokens=4096, but
    _compute_max_tokens caps new tokens at (model_context_size - num_prefix_tokens),
    so the EFFECTIVE training context was 2K — total budget never exceeded 2048.
  * R1-distill base ships max_position_embeddings=131072. Eval-time
    max_model_len MUST be set explicitly to honestly match training. We pass
    --max-model-len 2048 (or 4096 for the extrapolation probe).

Sampling: T=0.6, top_p=0.95 — matches SPO's evaluate_long_cot.sh and the
paper's published Qwen-1.5B MATH-500 numbers.

Grader (--grader, default 'both'; scores both on the SAME rollouts):
  * math_verify (PRIMARY) = SPO's PUBLISHED extractive_match grader (open-r1
    custom|math_500 config: robust parse + sympy verify), via _math_verify_grader.py.
    With T=0.6/top_p=0.95 this matches SPO's published MATH-500 protocol (grader +
    sampling); the differences from the paper's headline are mean@16 vs n=1 (a lower-
    variance estimate of the same pass@1) and that this is the user's seed42 SPO-tree
    run, not the released checkpoint. Reference: SPO-tree-666 extractive_match =
    0.736 @2K, 0.828 @4K, 0.848 @32K.
  * verl = Hendrycks is_equiv (last-boxed + strip_string + literal ==; NO sympy) —
    the legacy grader, recorded under result["graders"]["verl"] for continuity.
This grader change is SCOPED TO THIS (Qwen-1.5B) EVAL ONLY; eval_spo_deepseek7b.py is
unchanged and always grades with verl. The R1-distill chat template makes responses
emit <think>...</think>; the final \\boxed{} appears after </think>, and math_verify
also recovers a non-boxed final answer if the box is truncated.

Parquets: SPO/data/verl_qwen/{ds}_test.parquet, generated by
build_qwen_templated_parquets.py (re-wraps grpocredit's parquets with the
R1-distill <｜User｜>...<｜Assistant｜><think>\\n template).

Output: SPO/results/eval_spo_qwen1b_seed42.json.

Usage:
  python scripts/eval_spo_qwen1b.py [--dry-run] [--grader {both,math_verify,verl}]
"""

from __future__ import annotations

import argparse
import sys
from pathlib import Path

sys.path.insert(0, str(Path(__file__).resolve().parent))
from _eval_orchestrator import (  # noqa: E402
    RunSpec, WorkItem, add_common_args, discover_checkpoints, run_orchestrator,
)

REPO_ROOT = Path(__file__).resolve().parent.parent

CHECKPOINTS_ROOT = Path(
    "/lustre-storage/checkpoints/zengh/spo/spo_tree_qwen1b_MATH_seed42/"
    "qwen1.5b-spo-tree-666-s42/checkpoints"
)
BASE_SNAPSHOT = Path(
    "/lustre-storage/datasets/zengh/huggingface/hub/"
    "models--deepseek-ai--DeepSeek-R1-Distill-Qwen-1.5B/snapshots/"
    "ad9f0ae0864d7fbcd1cd905e3c6c5b069cc8b562"
)
OUT_JSON = REPO_ROOT / "results" / "eval_spo_qwen1b_seed42.json"

QWEN_PARQUET_DIR = REPO_ROOT / "data" / "verl_qwen"
PARQUETS = {
    "math_v2": QWEN_PARQUET_DIR / "math_v2_test.parquet",
    "aime24": QWEN_PARQUET_DIR / "aime24_test.parquet",
    "olympiadbench": QWEN_PARQUET_DIR / "olympiadbench_test.parquet",
    "collegemath": QWEN_PARQUET_DIR / "collegemath_test.parquet",
}

TEMPERATURE = 0.6
TOP_P = 0.95


def build_work_list(ckpts: list[tuple[int, int, Path]]) -> list[WorkItem]:
    items: list[WorkItem] = []
    final_four = ckpts[-4:]

    # 2K context, max_tokens=2048 (training-matched effective budget).
    # MATH-500 on all 40
    for it, step, path in ckpts:
        items.append(WorkItem(
            ckpt_iter=it, ckpt_step=step, ckpt_path=path,
            dataset="math_v2", n=16,
            temperature=TEMPERATURE, top_p=TOP_P,
            max_tokens=2048, max_model_len=2048,
            parquet=PARQUETS["math_v2"],
        ))
    # OOD slate on final 4 (still at 2K)
    ood_slate = [("aime24", 32), ("olympiadbench", 16), ("collegemath", 16)]
    for it, step, path in final_four:
        for ds, n in ood_slate:
            items.append(WorkItem(
                ckpt_iter=it, ckpt_step=step, ckpt_path=path,
                dataset=ds, n=n,
                temperature=TEMPERATURE, top_p=TOP_P,
                max_tokens=2048, max_model_len=2048,
                parquet=PARQUETS[ds],
            ))

    # 4K extrapolation probe — MATH-500 only, final 4 ckpts.
    for it, step, path in final_four:
        items.append(WorkItem(
            ckpt_iter=it, ckpt_step=step, ckpt_path=path,
            dataset="math_v2", n=16,
            temperature=TEMPERATURE, top_p=TOP_P,
            max_tokens=4096, max_model_len=4096,
            parquet=PARQUETS["math_v2"],
        ))
    return items


def main() -> None:
    ap = argparse.ArgumentParser()
    add_common_args(ap)
    ap.add_argument(
        "--grader",
        choices=["math_verify", "verl", "both"],
        default="both",
        help="Grader for this (Qwen-1.5B) eval. Default 'both': PRIMARY = "
             "math_verify (SPO's published extractive_match, open-r1 math_500 "
             "config), also records the legacy verl number. 'math_verify' = SPO "
             "grader only. 'verl' = legacy Hendrycks only. (The 7B eval is "
             "unaffected — it always uses verl.)",
    )
    ap.add_argument("--mv-precision", type=int, default=5,
                    help="float_rounding for math_verify (open-r1 math_500 used 5).")
    args = ap.parse_args()

    for ds, p in PARQUETS.items():
        if not p.is_file():
            print(f"[FATAL] missing Qwen-templated parquet for {ds}: {p}", file=sys.stderr)
            print(f"  build via: python scripts/build_qwen_templated_parquets.py", file=sys.stderr)
            sys.exit(3)

    if not CHECKPOINTS_ROOT.is_dir():
        print(f"[FATAL] missing checkpoints root: {CHECKPOINTS_ROOT}", file=sys.stderr)
        sys.exit(3)
    if not BASE_SNAPSHOT.is_dir():
        print(f"[FATAL] missing base snapshot: {BASE_SNAPSHOT}", file=sys.stderr)
        sys.exit(3)

    ckpts = discover_checkpoints(CHECKPOINTS_ROOT)
    print(f"[qwen1b] discovered {len(ckpts)} checkpoints", file=sys.stderr)
    if len(ckpts) < 4:
        print(f"[FATAL] need at least 4 ckpts for OOD slate; got {len(ckpts)}", file=sys.stderr)
        sys.exit(3)

    work = build_work_list(ckpts)
    spec = RunSpec(
        model_label="spo_tree_qwen1b_R1_distill_MATH_seed42",
        checkpoints_root=CHECKPOINTS_ROOT,
        base_model_snapshot=BASE_SNAPSHOT,
        out_json=OUT_JSON,
        work_items=work,
        # R1-Distill-Qwen-1.5B has 12 attention heads; the RunSpec default TP=8
        # (inherited from the 7B sibling, which has 32) fails vLLM's
        # heads % TP == 0 check at LLM() init. 12 is divisible by 1/2/4 only;
        # TP=1 is valid and most efficient for a 1.5B on a single H200.
        tensor_parallel_size=1,
        grader=args.grader,
        mv_precision=args.mv_precision,
        # SPO's lighteval custom|math_500 uses stop_sequence=[] (only EOS). The worker
        # default ["\n\n\nProblem:"] is a DeepSeekMath-SFT artifact (wrong for R1-distill
        # Qwen); disable it so generation matches SPO's published protocol exactly.
        stop=[],
    )
    run_orchestrator(spec, dry_run=args.dry_run)


if __name__ == "__main__":
    main()
